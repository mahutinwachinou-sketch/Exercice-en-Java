public class SommeDeuxNombres {

    public static void main(String[] args) {

        // Déclaration des deux nombres
        int nombre1 = 40;
        int nombre2 = 7;

        // Calcul de la somme
        int somme = nombre1 + nombre2;

        // Affichage du résultat
        System.out.println("La somme est : " + somme);
    }
}